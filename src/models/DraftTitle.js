/**
 * Draft title.
 */
class DraftTitle {
  /**
   * Title name.
   * @type {string}
   */
  name

  /**
   * Title URL.
   * @type {string}
   */
  url

  /**
   * Title created date.
   * @type {string}
   */
  dateCreated

  /**
   * Title modified date.
   * @type {(string|null)}
   */
  dateModified
}

module.exports = DraftTitle
